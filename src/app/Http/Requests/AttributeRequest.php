<?php

namespace Gustavo82mdq\Eav\app\Http\Requests;

class AttributeRequest extends \Backpack\CRUD\app\Http\Requests\CrudRequest
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        // only allow updates if the user is logged in
        return \Auth::check();
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        $rules = [
            'name' => 'required|string',
            'type' => 'required|string',
            'description' => 'nullable|string'
        ];

        if ($this->method() == 'POST') {
                    $rules['slug'] = 'required|alpha_dash|unique:'.config('rinvex.attributable.tables.attributes').',slug';
        }


        return $rules;
    }

    /**
     * Get the validation attributes that apply to the request.
     *
     * @return array
     */
    public function attributes()
    {
        return [
            //
        ];
    }

    /**
     * Get the validation messages that apply to the request.
     *
     * @return array
     */
    public function messages()
    {
        return [
            //
        ];
    }
}
